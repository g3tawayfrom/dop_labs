#include <stdio.h>

int main3()
{
    int a, b;

    // %o - форматный символ восьмеричного целого числа
    scanf("%o", &a);

    // %d - десятичного числа
    printf("%d\n", a);

    // \n - переход строки
    // %x - шестнадцатеричное число
    // >> - оператор битового сдвига вправо
    printf("%x %x\n", a, a >> 4);

    // ~ - оператор битового отрицания
    printf("%x %x\n", a, ~a);

    scanf("%x", &b);

    // & - оператор логического "И"
    printf("%x\n", (a & b));

    return 0;
}